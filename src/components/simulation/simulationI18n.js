export function getSimulationPartDetails(locale, selectedPart) {
  if (!selectedPart) return null
  const content = getContent(locale)
  const detail = content.partInsights[selectedPart]
  const part = content.parts[selectedPart]

  return {
    ...part,
    ...detail,
    ...detailToneMap[selectedPart],
  }
}

export function getSimulationToolProfile(locale, selectedTool) {
  const content = getContent(locale)
  return content.toolProfiles[selectedTool] ?? content.toolProfiles.select
}
