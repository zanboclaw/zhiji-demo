export function resolveSimulationRuntimeMessage(locale, message) {
  const content = getContent(locale)
  const template = content.runtimeMessages[message.contentKey]

  if (!template) return message.content ?? ''

  return template.replace(/\{(\w+)\}/g, (_, key) => message.vars?.[key] ?? '')
}

