export function getDashboardCopy(locale) {
  return dashboardContent[locale] ?? dashboardContent.en
}

export function resolveDashboardRuntimeMessage(locale, entry) {
  const content = getDashboardCopy(locale)
  const template = content.runtimeMessages[entry.messageKey]

  if (!template) return entry.message ?? ''

  return template.replace(/\{(\w+)\}/g, (_, key) => entry.vars?.[key] ?? '')
}
