export function getMarketplaceCopy(locale) {
  const content = marketplaceContent[locale] ?? marketplaceContent.en
  const fallback = marketplaceContent.en

  return {
    ...content,
    filters: {
      ...content.filters,
      sortOptions: content.filters.sortOptions ?? fallback.filters.sortOptions,
      categories: content.filters.categories ?? fallback.filters.categories,
    },
    skillsData: baseSkills.map((skill) => {
      const localizedSkill = content.skills[skill.id] ?? fallback.skills[skill.id]

      return {
        ...skill,
        ...localizedSkill,
        author: content.authors[skill.authorKey] ?? fallback.authors[skill.authorKey],
        devices: localizedSkill.devices ?? skill.devices,
      }
    }),
  }
}
